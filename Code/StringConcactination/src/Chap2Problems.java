
public class Chap2Problems {
	/*
	 * This is about how variables work and are used in java.
	 */

	public static void main(String[] args) {
		//Declaration of variables.
		
		double gpa;//This line creates a space for the variable.
		
		gpa = 3.75;//This line assigns a value for the variable.
		
		//double gpa = 3.75; is a way to combine the two lines.
		
		char gender; //Space to store single character.
		
		gender = 'M';
		
		int age; //Space to store non-decimal number.
		
		age = 26;
		
		boolean boolVar; //Space to store true or false value.
		
		boolVar = true;
		
		System.out.println(gpa);
		System.out.println(gender);
		System.out.println(age);
		System.out.println(boolVar);
	}

}
