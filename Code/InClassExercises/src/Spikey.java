
public class Spikey {

	/**
	 * This is an exercise on how to print to the console.
	 */
	public static void main(String[] args) {
		// using println to go to the next line and \\ to show \.
		System.out.println("  \\/");
		System.out.println(" \\\\//");
		System.out.println("\\\\\\///");
		System.out.println("///\\\\\\");
		System.out.println(" //\\\\");
		System.out.println("  /\\");

	}

}
